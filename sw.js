// Service Worker para E2E-Commerce PWA
const CACHE_NAME = 'e2e-commerce-v1';
const urlsToCache = [
  '/e2ecommerce/',
  '/e2ecommerce/index.html',
  '/e2ecommerce/demo.html',
  '/e2ecommerce/styles.css',
  '/e2ecommerce/script.js',
  '/e2ecommerce/manifest.json',
  '/e2ecommerce/cloudflare-integration.js',
  'https://cdnjs.cloudflare.com/ajax/libs/font-awesome/6.0.0/css/all.min.css'
];

// Install event
self.addEventListener('install', event => {
  event.waitUntil(
    caches.open(CACHE_NAME)
      .then(cache => {
        console.log('Cache opened');
        return cache.addAll(urlsToCache);
      })
  );
});

// Fetch event
self.addEventListener('fetch', event => {
  event.respondWith(
    caches.match(event.request)
      .then(response => {
        // Return cached version or fetch from network
        return response || fetch(event.request);
      }
    )
  );
});

// Activate event
self.addEventListener('activate', event => {
  event.waitUntil(
    caches.keys().then(cacheNames => {
      return Promise.all(
        cacheNames.map(cacheName => {
          if (cacheName !== CACHE_NAME) {
            console.log('Deleting old cache:', cacheName);
            return caches.delete(cacheName);
          }
        })
      );
    })
  );
});

